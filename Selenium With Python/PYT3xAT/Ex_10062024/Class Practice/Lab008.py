# Logical Operator - bool
x = 10
y = 20
print(x > y)
print(x < y)

a = 10
b = 10
print(a >= b)  # 10 > 10 or 10 = 10, returns bool
print(a == b)  # 10 > 10 or 10 = 10, returns bool
print(not a, "\"Operator: Not of a\"") # prints False

f = False
t = True
print("AND operator f AND t:", f and t) # Truth Table of AND gate
print("OR operator f OR t:", f or t) # Truth Table of OR gate
print("NOT operator of f:", not f) # Truth Table of NOT gate